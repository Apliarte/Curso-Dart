// Ejercicio 39
// En este ejercicio, se muestra cómo se pueden utilizar los bucles while en Dart.
// Aquí, se declara un bucle while que imprime los números del 1 al 5.

void example39() {
  var i = 1;
  while (i <= 5) {
    print("Example39 while loop '${i}'");
    i++;
  }
}

void main() {
  example39();
}